import { forwardRef, type ButtonHTMLAttributes, type HTMLAttributes, type ReactNode } from "react";
import { Hexagon, Sparkles, Loader2 } from "lucide-react";

/**
 * Single source of truth for the NECTAR visual signature.
 *
 * Brand tokens:
 *   navy surface  #141a3d  (--navy-800)
 *   navy deep     #0d112b  (--navy-900)
 *   amber accent  #f4a93a / #F2A23C  (--amber-500)
 *   amber-on-navy text  #f4a93a
 *   amber-pill text     #412402 (dark cocoa for AA on amber)
 *
 * Use these components anywhere NECTAR generates, summarizes,
 * suggests, or assists so the brand reads instantly.
 */

const cn = (...c: Array<string | false | null | undefined>) => c.filter(Boolean).join(" ");

/* -------------------------------------------------------------------------- */
/* NectarMark — circular hexagon+sparkle avatar (the "face" of NECTAR)        */
/* -------------------------------------------------------------------------- */

export function NectarMark({
  size = "md",
  className,
}: {
  size?: "sm" | "md" | "lg";
  className?: string;
}) {
  const dims = size === "sm" ? "h-7 w-7" : size === "lg" ? "h-16 w-16" : "h-10 w-10";
  const hex = size === "sm" ? "h-4 w-4" : size === "lg" ? "h-9 w-9" : "h-5 w-5";
  const spark = size === "sm" ? "h-2 w-2" : size === "lg" ? "h-3.5 w-3.5" : "h-2.5 w-2.5";
  return (
    <span
      aria-hidden
      className={cn(
        "relative inline-flex shrink-0 items-center justify-center rounded-full",
        "bg-[#0d112b] text-[#f4a93a] shadow-[0_4px_14px_-6px_rgba(13,17,43,0.6)] ring-1 ring-[#f4a93a]/30",
        dims,
        className,
      )}
    >
      <Hexagon className={cn(hex)} fill="currentColor" strokeWidth={1.25} />
      <span className="absolute -right-0.5 -top-0.5 inline-flex items-center justify-center rounded-full bg-[#f4a93a] p-[2px] text-[#412402] shadow-sm">
        <Sparkles className={spark} strokeWidth={2.5} />
      </span>
    </span>
  );
}

/* -------------------------------------------------------------------------- */
/* NectarBadge — pill wordmark "✨ NECTAR" on amber, the recognizable tag     */
/* -------------------------------------------------------------------------- */

export function NectarBadge({
  size = "md",
  live = false,
  label = "NECTAR",
  className,
}: {
  size?: "xs" | "sm" | "md";
  live?: boolean;
  label?: string;
  className?: string;
}) {
  const pad =
    size === "xs"
      ? "px-2 py-0.5 text-[9px]"
      : size === "sm"
      ? "px-2.5 py-1 text-[10px]"
      : "px-3 py-1 text-[11px]";
  const ic = size === "xs" ? "h-2.5 w-2.5" : "h-3 w-3";
  return (
    <span
      className={cn(
        "inline-flex items-center gap-1.5 rounded-full bg-[image:var(--gradient-amber)]",
        "font-bold uppercase tracking-wider text-[#412402]",
        "shadow-[0_1px_0_rgba(255,255,255,0.35)_inset]",
        pad,
        className,
      )}
    >
      <Sparkles className={ic} strokeWidth={2.5} />
      <span>{label}</span>
      {live && (
        <span className="ml-1 inline-flex items-center gap-1 text-[#412402]/80">
          <span className="relative inline-flex h-1.5 w-1.5">
            <span className="absolute inline-flex h-full w-full animate-ping rounded-full bg-[#1f7a4d] opacity-70" />
            <span className="relative inline-flex h-1.5 w-1.5 rounded-full bg-[#15a06a]" />
          </span>
          <span className="text-[9px] font-bold uppercase tracking-wider">live</span>
        </span>
      )}
    </span>
  );
}

