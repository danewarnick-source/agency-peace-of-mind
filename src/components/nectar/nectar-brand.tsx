/* -------------------------------------------------------------------------- */
/* NectarHeader — branded section header (mark + title + NECTAR badge)        */
/* -------------------------------------------------------------------------- */

export function NectarHeader({
  title,
  eyebrow,
  description,
  markSize = "md",
  surface = "navy",
  right,
  className,
}: {
  title: ReactNode;
  eyebrow?: ReactNode;
  description?: ReactNode;
  markSize?: "sm" | "md" | "lg";
  /** "navy" = dark NectarSurface header; "light" = light card header */
  surface?: "navy" | "light";
  right?: ReactNode;
  className?: string;
}) {
  if (surface === "navy") {
    return (
      <NectarSurface className={cn("p-5", className)}>
        <div className="flex flex-wrap items-center justify-between gap-3">
          <div className="flex items-center gap-3">
            <NectarMark size={markSize} />
            <div className="min-w-0">
              <div className="flex items-center gap-2">
                <NectarBadge size="xs" />
                {eyebrow && (
                  <span className="text-[10px] font-semibold uppercase tracking-wider text-[#fed7aa]/90">
                    {eyebrow}
                  </span>
                )}
              </div>
              <h2 className="mt-1 font-display text-lg font-bold tracking-tight text-white">
                {title}
              </h2>
              {description && (
                <p className="mt-0.5 text-sm text-white/75">{description}</p>
              )}
            </div>
          </div>
          {right}
        </div>
      </NectarSurface>
    );
  }
  return (
    <div className={cn("flex flex-wrap items-center justify-between gap-3", className)}>
      <div className="flex items-center gap-3">
        <NectarMark size={markSize} />
        <div className="min-w-0">
          <div className="flex items-center gap-2">
            <NectarBadge size="xs" />
            {eyebrow && (
              <span className="text-[10px] font-semibold uppercase tracking-wider text-[#7a4a0a]">
                {eyebrow}
              </span>
            )}
          </div>
          <h2 className="mt-1 font-display text-lg font-semibold tracking-tight text-[#0f1b3d]">
            {title}
          </h2>
          {description && (
            <p className="mt-0.5 text-sm text-muted-foreground">{description}</p>
          )}
        </div>
      </div>
      {right}
    </div>
  );
}

/* -------------------------------------------------------------------------- */
/* NectarButton — the canonical "Ask NECTAR" / "Run with NECTAR" CTA          */
/* -------------------------------------------------------------------------- */

type NectarButtonProps = ButtonHTMLAttributes<HTMLButtonElement> & {
  variant?: "primary" | "amber" | "ghost";
  loading?: boolean;
  icon?: ReactNode;
};

export const NectarButton = forwardRef<HTMLButtonElement, NectarButtonProps>(
  function NectarButton(
    { variant = "primary", loading, icon, children, className, disabled, ...rest },
    ref,
  ) {
    const base =
      "inline-flex min-h-[40px] items-center justify-center gap-2 rounded-md px-4 py-2 text-sm font-medium shadow-sm transition disabled:opacity-50 disabled:cursor-not-allowed focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-[#f4a93a]/50 focus-visible:ring-offset-2";
    const styles =
      variant === "amber"
        ? "bg-[image:var(--gradient-amber)] text-[#412402] hover:brightness-[1.04]"
        : variant === "ghost"
        ? "border border-[#f4a93a]/40 bg-[#f4a93a]/10 text-[#7a4a0a] hover:bg-[#f4a93a]/20"
        : "bg-[#0f1b3d] text-white hover:bg-[#1a2a5a]";
    const leading = loading ? (
      <Loader2 className="h-4 w-4 animate-spin" />
    ) : (
      icon ?? <Sparkles className="h-4 w-4" strokeWidth={2.5} />
    );
    return (
      <button
        ref={ref}
        {...rest}
        disabled={disabled || loading}
        className={cn(base, styles, className)}
      >
        {leading}
        {children}
      </button>
    );
  },
);
