/* -------------------------------------------------------------------------- */
/* NectarSurface — the navy panel background NECTAR sections sit on           */
/* -------------------------------------------------------------------------- */

export const NectarSurface = forwardRef<HTMLDivElement, HTMLAttributes<HTMLDivElement>>(
  function NectarSurface({ className, children, ...rest }, ref) {
    return (
      <div
        ref={ref}
        {...rest}
        className={cn(
          "relative overflow-hidden rounded-2xl border border-[#1f2752] bg-[#141a3d] text-white",
          "shadow-[0_12px_30px_-20px_rgba(13,17,43,0.6)]",
          className,
        )}
      >
        {/* signature amber sparkle wash in the top-right */}
        <span
          aria-hidden
          className="pointer-events-none absolute -right-10 -top-10 h-40 w-40 rounded-full"
          style={{
            background:
              "radial-gradient(closest-side, rgba(244,169,58,0.18), transparent 70%)",
          }}
        />
        {children}
      </div>
    );
  },
);

